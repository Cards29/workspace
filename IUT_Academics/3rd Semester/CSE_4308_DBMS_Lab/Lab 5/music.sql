DROP TABLE PERFORMS;
DROP TABLE INCLUDES;
DROP TABLE RATING;
DROP TABLE ARTIST;
DROP TABLE SONG;
DROP TABLE ALBUM;
DROP TABLE LISTENER;

CREATE TABLE ARTIST(
    ART_ID INTEGER,
    FIRST_NAME VARCHAR2(50),
    LAST_NAME VARCHAR2(50),
    GENDER VARCHAR2(10),
    CONSTRAINT pk_artist PRIMARY KEY(ART_ID)
);

CREATE TABLE ALBUM(
    ALB_ID INTEGER,
    TITLE VARCHAR2(60),
    RELEASE_YEAR VARCHAR2(20),
    LANGUAGE VARCHAR2(50),
    CONSTRAINT pk_album PRIMARY KEY(ALB_ID)
);

CREATE TABLE SONG(
    SONG_ID INTEGER,
    TITLE VARCHAR2(60),
    DURATION INTEGER,
    GENRE VARCHAR2(30),
    CONSTRAINT pk_song PRIMARY KEY(SONG_ID)
);

CREATE TABLE LISTENER(
    LIS_ID INTEGER,
    FIRST_NAME VARCHAR2(50),
    LAST_NAME VARCHAR2(50),
    CONSTRAINT pk_listener PRIMARY KEY(LIS_ID)
);

CREATE TABLE PERFORMS(
    ART_ID INTEGER,
    SONG_ID INTEGER,
    CONSTRAINT pk_performs PRIMARY KEY(ART_ID, SONG_ID),
    CONSTRAINT fk_performs_artist FOREIGN KEY(ART_ID) REFERENCES ARTIST(ART_ID),
    CONSTRAINT fk_performs_song FOREIGN KEY(SONG_ID) REFERENCES SONG(SONG_ID)
);

CREATE TABLE INCLUDES(
    ALB_ID INTEGER,
    SONG_ID INTEGER,
    CONSTRAINT pk_includes PRIMARY KEY(ALB_ID, SONG_ID),
    CONSTRAINT fk_includes_album FOREIGN KEY(ALB_ID) REFERENCES ALBUM(ALB_ID),
    CONSTRAINT fk_includes_song FOREIGN KEY(SONG_ID) REFERENCES SONG(SONG_ID)
);

CREATE TABLE RATING(
    SONG_ID INTEGER,
    LIS_ID INTEGER,
    RATING FLOAT,
    CONSTRAINT pk_rating PRIMARY KEY(SONG_ID, LIS_ID),
    CONSTRAINT fk_rating_song FOREIGN KEY(SONG_ID) REFERENCES SONG(SONG_ID),
    CONSTRAINT fk_rating_listener FOREIGN KEY(LIS_ID) REFERENCES LISTENER(LIS_ID)
);

